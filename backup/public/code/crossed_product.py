def crossed_product_Zn(algebra_basis, alpha_action, n, dim):
    """
    Construct M ⋊_α Z_n where alpha_action is a function mapping matrices to matrices
    (representing the automorphism alpha of order n).
    
    Returns:
      - generators of the crossed product algebra (acting on C^dim ⊗ C^n)
      - the shift unitary U
    """
    # Build pi(a) for each a in algebra_basis
    # pi(a) = sum_{k=0}^{n-1} alpha^k(a) ⊗ P_k
    # where P_k is |k><k| in C^n
    
    new_dim = dim * n
    crossed_basis = []
    
    # Need alpha_inverse to build pi(a) = sum_k alpha_{-k}(a) ⊗ |k><k|
    # (This is the correct sign convention for U pi(a) U^{-1} = pi(alpha(a)).)
    # Since we only have alpha and alpha^n = id, alpha_{-1} = alpha_{n-1}.
    
    for a in algebra_basis:
        # Compute all alpha_{-k}(a) for k = 0, ..., n-1
        # alpha^{-k}(a) = alpha^{n-k}(a) mod n
        alpha_powers_pos = [a]  # alpha^0, alpha^1, ..., alpha^{n-1}
        current = a
        for _ in range(n - 1):
            current = alpha_action(current)
            alpha_powers_pos.append(current)
        
        # alpha_{-k} = alpha_{n-k} mod n; for k=0 it's alpha^0 = id
        alpha_neg_powers = [alpha_powers_pos[(-k) % n] for k in range(n)]
        
        # Build pi(a) = sum_k alpha_{-k}(a) ⊗ |k><k|
        pi_a = np.zeros((new_dim, new_dim), dtype=complex)
        for k in range(n):
            P_k = np.zeros((n, n), dtype=complex)
            P_k[k, k] = 1.0
            pi_a += np.kron(alpha_neg_powers[k], P_k)
        
        crossed_basis.append(pi_a)
    
    # Shift operator S: |k> -> |k+1 mod n>
    S = np.zeros((n, n), dtype=complex)
    for k in range(n):
        S[(k + 1) % n, k] = 1.0
    
    U = np.kron(np.eye(dim), S)
    
    # The crossed product is generated by {pi(a) : a in M} ∠ {U}
    generators = crossed_basis + [U]
    
    return generators, U


def verify_covariance(algebra_basis, alpha_action, n, dim):
    """
    Verify that U pi(a) U^{-1} = pi(α(a)) on the crossed product.
    This is the defining relation.
    """
    generators, U = crossed_product_Zn(algebra_basis, alpha_action, n, dim)
    pi_basis = generators[:-1]  # everything except U
    
    U_inv = U.conj().T
    
    max_error = 0.0
    for i, a in enumerate(algebra_basis):
        alpha_a = alpha_action(a)
        # pi(alpha(a)) = sum_k alpha^{-k}(alpha(a)) ⊗ |k><k|
        #             = sum_k alpha^{1-k}(a) ⊗ |k><k|
        alpha_a_powers_pos = [alpha_a]
        current = alpha_a
        for _ in range(n - 1):
            current = alpha_action(current)
            alpha_a_powers_pos.append(current)
        
        # For pi(alpha(a)): the k-th slot is alpha^{1-k}(a) = alpha^{-k}(alpha(a))
        # which in our list (indexed by power of alpha(a)) is alpha_a_powers_pos[(-k) % n]
        alpha_a_neg_powers = [alpha_a_powers_pos[(-k) % n] for k in range(n)]
        
        pi_alpha_a = np.zeros((dim * n, dim * n), dtype=complex)
        for k in range(n):
            P_k = np.zeros((n, n), dtype=complex)
            P_k[k, k] = 1.0
            pi_alpha_a += np.kron(alpha_a_neg_powers[k], P_k)
        
        # Check U pi(a) U^{-1} = pi(α(a))
        lhs = U @ pi_basis[i] @ U_inv
        error = np.linalg.norm(lhs - pi_alpha_a)
        max_error = max(max_error, error)
    
    return max_error


