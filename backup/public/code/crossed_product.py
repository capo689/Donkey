"""
Crossed product: M ⋊_α Z_n implementation and tests.

Mathematical setup:
- M: a von Neumann algebra acting on Hilbert space H
- alpha: an automorphism of M of period n (so α^n = identity on M)
- The crossed product M ⋊_α Z_n acts on H ⊗ C^n
- Generators:
  - pi(a) = a ⊗ P_0 + α(a) ⊗ P_1 + α^2(a) ⊗ P_2 + ... + α^{n-1}(a) ⊗ P_{n-1}
    where P_k is the projection onto |k> in C^n
  - U = I ⊗ S where S is the cyclic shift on C^n (S|k> = |k+1 mod n>)
- These satisfy the covariance relation: U pi(a) U^{-1} = pi(α(a))

The "Z_n" version is the natural finite-dim analog of M ⋊_α R, which requires
an infinite-dim clock. Taking n → infinity with appropriate scaling gives R.

For the CLPW application:
- M = CFT algebra (our finite-dim proxy)
- alpha = modular flow (discretized: one step = some fraction of 2π in imaginary time)
- n = number of discrete clock states
- The crossed product is the "observer-inclusive" algebra
"""

