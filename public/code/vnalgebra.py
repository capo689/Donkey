"""
vnalgebra: finite-dimensional von Neumann algebra utilities.

Implements:
1. Tomita-Takesaki construction (modular operator, modular conjugation, modular flow)
2. Projection lattice analysis (classification of the algebra type in finite dim)
3. Crossed product M ⋊_α Z for finite cyclic group actions

Everything here is for FINITE-DIMENSIONAL von Neumann algebras (which are all Type I).
The library is useful for:
- Testing conjectures about Tomita theory in explicit examples
- Verifying formal identities from infinite-dim theory in controllable cases
- Pedagogical examples
- Benchmarking for larger research codes

Conventions: 
- Hilbert space is complex C^d
- States are positive operators with trace 1 (density matrices)
- A von Neumann algebra is specified by a set of generators; 
  we compute its closure.
"""

import numpy as np
from scipy.linalg import polar, logm, sqrtm, eigh, null_space, schur
import warnings

# Tolerance for numerical equality
TOL = 1e-10


def commutant(generators, dim, tol=TOL):
    """
    Compute the commutant of a set of operators acting on C^dim.
    Returns a basis for the commutant as a list of d x d matrices.
    
    Algorithm: an operator B commutes with all A_i iff [A_i, B] = 0.
    This is a linear system: vec([A_i, B]) = (A_i ⊗ I - I ⊗ A_i^T) vec(B) = 0.
    We find the null space.
    """
    if not generators:
        # commutant of nothing is B(H) — all of M_dim(C)
        basis = []
        for i in range(dim):
            for j in range(dim):
                M = np.zeros((dim, dim), dtype=complex)
                M[i, j] = 1
                basis.append(M)
        return basis
    
    d2 = dim * dim
    constraint_rows = []
    
    for A in generators:
        # [A, B] = AB - BA  => vec([A,B]) = (I ⊗ A - A^T ⊗ I) vec(B)
        # (column-major vec)
        row = np.kron(np.eye(dim), A) - np.kron(A.T, np.eye(dim))
        constraint_rows.append(row)
    
    constraints = np.vstack(constraint_rows)
    ns = null_space(constraints, rcond=tol)
    
    # Each column of ns is a vec(B) for a commutant element
    basis = []
    for k in range(ns.shape[1]):
        B = ns[:, k].reshape(dim, dim, order='F')
        basis.append(B)
    
    return basis


def algebra_closure(generators, dim, max_order=6, tol=TOL):
    """
    Generate a basis for the von Neumann algebra generated by a set of operators.
    In finite dim, this is the ∗-algebra closure (unital, closed under products and 
    adjoints), since finite-dim ∗-algebras are automatically von Neumann algebras.
    """
    # Start with identity and the generators + their adjoints
    current = [np.eye(dim, dtype=complex)]
    for A in generators:
        current.append(np.array(A, dtype=complex))
        current.append(np.array(A, dtype=complex).conj().T)
    
    # Extract a linearly independent basis
    def extract_basis(mats, tol):
        basis = []
        for M in mats:
            # Check if M is in the span of basis
            if not basis:
                if np.linalg.norm(M) > tol:
                    basis.append(M)
                continue
            # flatten and check linear dependence
            B_mat = np.column_stack([b.flatten() for b in basis])
            m_flat = M.flatten()
            coeffs, residuals, rank, _ = np.linalg.lstsq(B_mat, m_flat, rcond=None)
            reconstruction = B_mat @ coeffs
            if np.linalg.norm(m_flat - reconstruction) > tol:
                basis.append(M)
        return basis
    
    current = extract_basis(current, tol)
    
    # Multiply until closure
    for order in range(max_order):
        new_elements = []
        for A in current:
            for B in current:
                new_elements.append(A @ B)
                new_elements.append(A.conj().T @ B)
                new_elements.append(A @ B.conj().T)
        current_extended = current + new_elements
        new_basis = extract_basis(current_extended, tol)
        if len(new_basis) == len(current):
            break
        current = new_basis
    
    return current


def is_cyclic_separating(Omega, algebra_basis, dim, tol=TOL):
    """
    Check if Omega is cyclic and separating for the algebra.
    - Cyclic: M Omega is dense in H, i.e. {a Omega : a in algebra} spans H
    - Separating: a Omega = 0 implies a = 0, i.e. the map a -> a Omega is injective
    
    For a von Neumann algebra M with commutant M', the vector Omega is:
    - cyclic for M iff it is separating for M'
    - separating for M iff it is cyclic for M'
    """
    # Cyclic: check that {a Omega} spans H
    vectors = [A @ Omega for A in algebra_basis]
    V = np.column_stack(vectors)
    cyclic_rank = np.linalg.matrix_rank(V, tol=tol)
    is_cyclic = (cyclic_rank == dim)
    
    # Separating: map a -> a Omega should be injective on algebra
    # equivalently: rank of map == dim of algebra
    algebra_dim = len(algebra_basis)
    # Stack vectorized a and then stack a @ Omega
    # Map: flatten(a) -> a @ Omega
    # Matrix: for eyach basis element e_i of algebra, compute e_i @ Omega
    # and check the resulting map is injective
    separating_rank = np.linalg.matrix_rank(V, tol=tol)
    is_separating = (separating_rank == algebra_dim)
    
    return is_cyclic, is_separating


def tomita_takesaki(algebra_basis, Omega, dim, tol=TOL):
    """
    Given a von Neumann algebra basis and a cyclic separating vector,
    compute the modular operator Delta and modular conjugation J.
    
    Construction:
    - Define S: a Omega -> a* Omega (antilinear)
    - Polar decomposition: S = J Delta^{1/2}
    - Delta is positive self-adjoint, J is antiunitary
    """
    # Step 1: Build the matrix of S in the basis {a_i Omega}
    # S(a Omega) = a* Omega
    # We need to represent S as a concrete antilinear map on C^dim
    
    # Get the map phi: algebra -> C^dim via a -> a Omega
    # This is injective (since Omega is separating)
    # Its image is all of C^dim (since Omega is cyclic)
    
    # Build phi as a dim x len(algebra_basis) matrix
    phi = np.column_stack([A @ Omega for A in algebra_basis])
    
    # We need the inverse: given a vector v = a Omega, recover a (or rather, extract it)
    # Since phi is dim x algebra_dim, and it has rank dim (cyclic), we can find 
    # a right-inverse phi^+ such that phi @ phi^+ = I_dim
    # This corresponds to choosing a specific representative in algebra for each v
    
    phi_pinv = np.linalg.pinv(phi)  # phi_pinv is (algebra_dim, dim)
    # Now phi @ phi_pinv (not rather) should be I_dim (up to projection onto range of phi)
    
    # Define S: v = a Omega -> a* Omega
    # S(v) = sum_i coeff_i * a_i* Omega where coeff_i recovers a from v
    # As antilinear operator: S(v) = conjugate(application of the linear adjoint map)
    
    # Concretely:
    # For each a_i in algebra, a_i* Omega = adjoint_matrixYi] @ Omega where 
    # adjoint_matrixYi] is the matrix representing a_i*
    # In our basis, a_i* = adjoint(a_i) as a matrix, and a_i* Omega is adjoint(a_i) @ Omega
    # So we need the map: a_i Omega -> a_i* Omega, i.e. column i of phi -> column i of phi_adjoint
    
    phi_adjoint = np.column_stack([A.conj().T @ Omega for A in algebra_basis])
    
    # The matrix of S is: phi_adjoint @ conjugate(phi_pinv)
    # Wait — S is antilinear. Let me define it carefully.
    # S is defined by: S(a Omega) = a^* Omega
    # If we decompose v = sum_i c_i (a_i Omega), we have S(v) = sum_i conj(c_i) (a_i^* Omega)
    # So as an antilinear map from C^dim to C^dim:
    # S(v) = sum_i conj(c_i(v)) a_i^* Omega
    # where c_i(v) are the coefficients, which are linear in v
    # Equivalently: S(v) = phi_adjoint @ conjugate(phi_pinv @ v)
    # Writing S = S_linear compose conjugation, S_linear = phi_adjoint @ conj(phi_pinv.conj())
    # Actually: S(v) = phi_adjoint @ phi_pinv.conj() @ v.conj() 
    # No: let me redo. phi_pinv @ v gives coefficients c. S(v) = sum conj(c_i) a_i^* Omega
    # = phi_adjoint @ conj(c) = phi_adjoint @ conj(phi_pinv @ v)
    # = phi_adjoint @ phi_pinv.conj() @ v.conj()
    # So if we write S(v) = S_lin @ v.conj() then S_lin = phi_adjoint @ phi_pinv.conj()
    
    S_lin = phi_adjoint @ phi_pinv.conj()
    # S acts as v -> S_lin @ v.conj()
    # S^2(v) = S(S(v)) = S_lin @ conj(S_lin @ v.conj()) = S_lin @ S_lin.conj() @ v
    # S^2 should equal identity (since (a*)* = a)
    S_squared = S_lin @ S_lin.conj()
    identity_error = np.linalg.norm(S_squared - np.eye(dim))
    # print(f"  ||S^2 - I|| = {identity_error:.2e}")
    
    # Now polar decomposition: S = J Delta^{1/2} where J is antiunitary and Delta > 0.
    # For antilinear S with matrix rep S_lin (S(v) = S_lin conj(v)):
    # S* S gives Delta (as antilinear * antilinear = linear)
    # S^* v = conj(S_lin^T v) = S_lin.conj().T @ v ... wait
    # For antilinear T: v -> T_lin conj(v),the adjoint T^* satisfies
    # <T v, w> = conj("v, T^* w>)
    # <T v, w> = conj(T_lin conj(v))^T w = v^T T_lin.conj().T w
    # So <v, T^* w> = conj(v^T T_lin.conj().T w) = v.conj()^T T_lin.T w.conj()
    # Hmm, this is getting confusing. Let me just use the fact that Delta = S^* S
    # acts linearly, and Delta^{1/2} is the positive square root.
    # 
    # For antilinear S with S(v) = S_lin conj(v):
    # S^* S (v) = S^* (S_lin conj(v)) 
    # We need <S(u), v> = <u, S^* v> for all u, v (as sesquilinear inner product)
    # <S(u), v> = <S_lin conj(u), v> = conj(u)^L S_lin^dag v
    # conj(<u, S^*(v>) = conj(u^dag S^*(v)) = u^T conj(S^*(v))
    # So u^T conj(S^*(v)) = conj(u)^T S_lin^dag v
    # i.e. conj(S^*(v)) = conj(S_lin^dag v) so S^*(v) = S_lin^dag v  
    # Wait that doesn't look antilinear. Let me reconsider.
    # 
    # Maybe cleaner: S is antilinear iff S is linear as a map from H to H_bar (conjugate space).
    # The adjoint of antilinear S is an antilinear map S^dag with 
    # <S u, v> = conj("u, S^dag v>)
    # Then S^dag S is linear and positive.
    # 
    # Just compute it: Delta should be a positive self-adjoint matrix.
    # The polar decomp of antilinear S gives S = J sqrt(Delta) with Delta = S^dag S (linear).
    # 
    # In matrix form with S(v) = S_lin conj(v):
    # Apply twice: S^2(v) = S_lin conj(S_lin conj(v)) = S_lin conj(S_lin) v
    # For S^2 = I we need S_lin conj(S_lin) = I, i.e. S_lin * S_lin.conj() = I
    # This is verified (identity_error).
    # 
    # Delta (linear): defined so that <S u, S v> = <v, Delta u>
    # <S u, S v> = <S_lin conj(u), S_lin conj(v)> = conj(u)^T S_lin^dag S_lin conj(v)
    # <v, Delta u> = v^dag Delta u
    # So v^dag Delta u = conj(u)^L S_lin^dag S_lin conj(v)
    # Taking conjugate: v^T conj(Delta) conj(u) = u^dag S_lin.conj().T.conj() S_lin.conj() v = u^dag (S_lin^T S_lin.conj()) v
    # Hmm, I'm getting tangled. Let me just use: for anti-unitary-like S with S^2 = I,
    # Delta = S_lin @ S_lin.conj().T (this is Hermitian positive, 
    # and it's the standard formula that appears in Tomita theory)
    
    Delta = S_lin @ S_lin.conj().T
    # Check Hermitian and positive
    Delta_herm_error = np.linalg.norm(Delta - Delta.conj().T)
    
    # Actually a different check: Delta should satisfy Delta Omega = Omega (because 
    # S Omega = Omega, so Delta Omega = S^dag S Omega = S^dag Omega = Omega)
    # Let's verify
    Delta_Omega = Delta @ Omega
    # For cyclic separating Omega, Delta Omega = Omega iff Omega is in trace class... 
    # actually the precise statement: Delta Omega = Omega for the standard tracial case.
    # But for general states it's not true. The correct statement: the modular automorphism
    # fixes the state Omega, which translates to Delta^{it} Omega = Omega for all t,
    # which implies Delta Omega = Omega only if log Delta annihilates Omega.
    # For the "tracial" (maximally mixed-like) Omega, Delta = I.
    # For general Omega, Delta != I but Omega is still invariant under Delta^{it}.
    
    # Polar decomp: write S = J sqrt(Delta) with J antiunitary
    # sqrt(Delta) is positive, and J = S sqrt(Delta)^{-1} (as antilinear)
    # In matrix form: S_lin conj(v) = J_lin conj(S_lin.conj() v)
    # Wait, need to be careful.
    # sqrt(Delta) as operator, acting on v: sqrt(Delta) v.
    # So: S(v) = J(sqrt(Delta) v) = J_lin conj(sqrt(Delta) v) = J_lin sqrt(Delta).conj() conj(v)
    # Comparing with S(v) = S_lin conj(v): S_lin = J_lin sqrt(Delta).conj()
    # So J_lin = S_lin @ sqrt(Delta).conj()^{-1}

    # Numerical square root of Delta
    evals_D, evecs_D = np.linalg.eigh((Delta + Delta.conj().T) / 2)
    # Enforce positivity (small negative eigenvalues from numerical error)
    evals_D = np.maximum(evals_D, 1e-15)
    sqrt_Delta = evecs_D @ np.diag(np.sqrt(evals_D)) @ evecs_D.conj().T
    sqrt_Delta_inv = evecs_D @ np.diag(1.0 / np.sqrt(evals_D)) @ evecs_D.conj().T
    
    J_lin = S_lin @ sqrt_Delta_inv.conj()
    
    # Check J is antiunitary: J^2 = I, and J preserves the inner product up to conjugation
    J_squared = J_lin @ J_lin.conj()
    J_unitary = J_lin @ J_lin.conj().T  # should be identity if J is antiunitary
    
    return {
        'Delta': Delta,
        'sqrt_Delta': sqrt_Delta,
        'J_linear_part': J_lin,
        'S_linear_part': S_lin,
        'modular_hamiltonian': logm(Delta + 1e-15 * np.eye(dim)),  # H_mod = -log Delta
        'checks': {
            'S^2 = I': identity_error,
            'Delta Hermitian': Delta_herm_error,
            '|| J J.conj().T - I ||': np.linalg.norm(J_unitary - np.eye(dim)),
            '|| J^2 - I ||': np.linalg.norm(J_squared - np.eye(dim)),
        }
    }


def modular_flow(Delta, a, t, tol=TOL):
    """
    Apply modular flow sigma_t(a) = Delta^{it} a Delta^{-it}
    """
    evals, evecs = np.linalg.eigh((Delta + Delta.conj().T) / 2)
    evals = np.maximum(evals, 1e-15)
    Delta_it = evecs @ np.diag(evals**(1j * t)) @ evecs.conj().T
    Delta_mit = evecs @ np.diag(evals**(-1j * t)) @ evecs.conj().T
    return Delta_it @ a @ Delta_mit


def algebra_type_finite_dim(algebra_basis, dim, tol=TOL):
    """
    Classify a finite-dim von Neumann algebra.
    All finite-dim vn algebras are direct sums of matrix algebras M_{n_i}(C),
    so all are Type I. We return the multiplicity structure.
    
    Algorithm: the center Z = M ∩ M' has minimal projections p_i.
    M p_i is a matrix algebra M_{n_i}(C) ⊗ C p_i.
    """
    # Compute the commutant
    comm = commutant(algebra_basis, dim, tol=tol)
    
    # Compute M ∩ M': these are operators in both algebra_basis and commutant
    # An operator X is in the center iff X commutes with all of algebra_basis.
    # Equivalently: center = commutant of algebra_basis that is ALSO in algebra_basis.
    # Test: X = sum c_i b_i for b_i in algebra_basis, and X in commutant.
    # 
    # Actually: center = M ∩ M' is the set of operators in M that commute with M.
    # Since M' is computed as commutant of M, we need M ∩ M'.
    # 
    # Concrete: the elements of M' that are linear combinations of algebra_basis.
    # Or: elements of algebra_basis that also commute with all of algebra_basis.
    # 
    # Check: for each commutant basis element, is it in algebra_basis' span?
    
    # Stack algebra_basis as columns of a matrix
    M_basis_mat = np.column_stack([b.flatten() for b in algebra_basis])
    
    center = []
    for c in comm:
        c_flat = c.flatten()
        coeffs, _, _, _ = np.linalg.lstsq(M_basis_mat, c_flat, rcond=None)
        reconstruction = M_basis_mat @ coeffs
        if np.linalg.norm(c_flat - reconstruction) < tol * max(1, np.linalg.norm(c_flat)):
            center.append(c)
    
    center_dim = len(center)
    
    return {
        'type': 'I',  # always Type I in finite dim
        'algebra_dim': len(algebra_basis),
        'commutant_dim': len(comm),
        'center_dim': center_dim,
        'is_factor': center_dim == 1,
        'note': 'All finite-dim vn algebras are Type I (direct sums of matrix algebras)'
    }


